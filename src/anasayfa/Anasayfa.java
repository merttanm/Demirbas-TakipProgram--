    private void demirbasAra(){
        int secilenAramaTipi = cmb_FiltreTipi.getSelectedIndex();
        boolean demirbasBulundumu=false;
        if(secilenAramaTipi==0){
            
            model.Demirbas arananDemirbas = db.demirbasiUrunKodunaGoreGetir(txt_DemirbasDetayliAra.getText());
            
            if(arananDemirbas!= null ){
                Demirbas demirbas = demirbasModelCevir(arananDemirbas);
                aramaEkraniniDoldur(demirbas);
                demirbasBulundumu = true;
            }
            
        }else if(secilenAramaTipi==1){
            model.Demirbas arananDemirbas = db.demirbasiUrunAdinaGoreGetir(txt_DemirbasDetayliAra.getText());
            if(arananDemirbas != null ){
                Demirbas demirbas = demirbasModelCevir(arananDemirbas);
                aramaEkraniniDoldur(demirbas);
                demirbasBulundumu = true;   
            }
        }
        if(demirbasBulundumu==false){
            JOptionPane.showMessageDialog(rootPane, "Aranan Demirbaş Bulunamadı", "Bulunamadı", JOptionPane.ERROR_MESSAGE);                    
        }
    }
    
    private void aramaEkraniniDoldur(Demirbas demirbas){
        
        temizle(txt_DemirbasDetayliAraAdet,txt_DemirbasDetayliAraBirimFiyati,txt_DemirbasDetayliAraKayitTarihi,txt_DemirbasDetayliAraUrunAdi
                ,txt_DemirbasDetayliAraUrunKategori,txt_DemirbasDetayliAraUrunKodu);
        txt_DemirbasDetayliAraZimmetli.setText("");
        
        txt_DemirbasDetayliAraAdet.setText(String.valueOf(demirbas.getAdet()));
        txt_DemirbasDetayliAraBirimFiyati.setText(""+demirbas.getBirimFiyati());
        txt_DemirbasDetayliAraKayitTarihi.setText(TarihCevir.convertStringToDate(demirbas.getKayitTarihi()));
        txt_DemirbasDetayliAraUrunAdi.setText(demirbas.getUrunAdi());
        txt_DemirbasDetayliAraUrunKategori.setText(demirbas.getUrunKategorisi());
        txt_DemirbasDetayliAraUrunKodu.setText(demirbas.getUrunKodu());
        
        
        txt_DemirbasDetayliAraZimmetli.setText(demirbas.getTeslimAlan().getPersonelAdi()+ "  " + demirbas.getTeslimAlan().getPersonelSoyadi());
    }
    


