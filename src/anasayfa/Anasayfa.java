        public void demirbaslariListedenSil(){
            DefaultTableModel model = (DefaultTableModel) tbl_DemirbasListesi.getModel();
            int rowCount = model.getRowCount();
            for (int i = rowCount - 1; i >= 0; i--) {
                model.removeRow(i);
            }
        }
        public void demirbaslariListele()
        {
//            demirbaslariListedenSil();
            DefaultTableModel model = (DefaultTableModel) tbl_DemirbasListesi.getModel();
            
            List<Demirbas> list = new ArrayList<>();
            for(model.Demirbas demirbasModel : db.butunDemirbaslariGetir()){
                Demirbas demirbas = demirbasModelCevir(demirbasModel);
                list.add(demirbas);
            } 
            Object rowData[] = new Object[4];
            for(int i = 0; i < list.size(); i++)
            {
                rowData[0] = list.get(i).getUrunKodu();
                rowData[1] = list.get(i).getUrunAdi();
                rowData[2] = list.get(i).getUrunKategorisi();
                rowData[3] = TarihCevir.convertStringToDate(list.get(i).getKayitTarihi());
                model.addRow(rowData);
            }       
            
        }

